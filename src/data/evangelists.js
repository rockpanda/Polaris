export function getProvinceMapData(isWorldMap = true) {
  const provinceCount = {}
  console.log('Processing evangelists:', evangelists)
  
  // 遍历所有使者，统计每个省份/地区的数量
  evangelists.forEach(e => {
    const province = cityToProvince[e.city]
    console.log(`Processing ${e.name} from ${e.city} -> ${province}`)
    if (province) {
      if (isWorldMap) {
        // 世界地图模式：所有中国地区合并为 中国
        if (['北京', '天津', '上海', '重庆', '石家庄', '太原', '沈阳', '长春', '哈尔滨', 
             '南京', '杭州', '合肥', '福州', '南昌', '济南', '郑州', '武汉', '长沙', 
             '广州', '深圳', '海口', '成都', '贵阳', '昆明', '西安', '兰州', '西宁', 
             '台北', '呼和浩特', '南宁', '拉萨', '银川', '乌鲁木齐', '香港', '澳门'].includes(e.city)) {
          provinceCount['China'] = (provinceCount['China'] || 0) + 1
        } else {
          // 海外地区
          if (province === '日本') {
            provinceCount['Japan'] = (provinceCount['Japan'] || 0) + 1
            console.log('Found evangelist in Japan:', e.name)
          } else if (province === '瑞典') {
            provinceCount['Sweden'] = (provinceCount['Sweden'] || 0) + 1
            console.log('Found evangelist in Sweden:', e.name)
          }
        }
      } else {
        // 中国地图模式：直接使用省份名称
        provinceCount[province] = (provinceCount[province] || 0) + 1
      }
    }
  })
  console.log('Raw provinceCount:', provinceCount)
  
  // 只返回有使者的地区数据
  const mapData = Object.entries(provinceCount)
    .filter(([_, value]) => value > 0)
    .map(([name, value]) => {
      let finalName = name
      if (isWorldMap) {
        // 世界地图模式：处理海外地区名称
        if (name === 'Japan') finalName = 'Japan'
        if (name === 'Sweden') finalName = 'Sweden'
        if (name === 'China') finalName = 'China'
      } else {
        // 中国地图模式：直接使用省份名称
        finalName = name
      }
      return { name: finalName, value }
    })
  
  console.log('Final mapData:', mapData)
  return mapData
} 